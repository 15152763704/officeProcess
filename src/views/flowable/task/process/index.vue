import {
  getDeployment,
  delDeployment,
  addDeployment,
  updateDeployment,
  exportDeployment,
  flowRecord,
} from '@/api/flowable/finished'
import { myProcessList, stopProcess } from '@/api/flowable/process'
import { listDefinition } from '@/api/flowable/definition'
import { getReqAction } from '@/utils/global'

export default {
  name: 'Deploy',
  components: {},
  data() {
    return {
      showTimetime:[],
      // 遮罩层
      loading: true,
      processLoading: true,
      // 选中数组
      ids: [],
      // 非单个禁用
      single: true,
      // 非多个禁用
      multiple: true,
      // 显示搜索条件
      showSearch: true,
      // 总条数
      total: 0,
      processTotal: 0,
      // 我发起的流程列表数据
      myProcessList: [],
      // 弹出层标题
      title: '',
      // 是否显示弹出层
      open: false,
      src: '',
      definitionList: [],
      // 查询参数
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        name: null,
        category: null,
        key: null,
        tenantId: null,
        deployTime: null,
        derivedFrom: null,
        derivedFromRoot: null,
        parentDeploymentId: null,
        engineVersion: null,
      },
      options: [{
        value: 'y',
        label: '进行中'
      }, {
        value: 'n',
        label: '已完成'
      }],
      // 表单参数
      form: {},
      // 表单校验
      rules: {},
    }
  },
  created() {
    this.getList()
  },
  methods: {
    //条件申请时间
    handletimeChangetime(dataarr) {
      this.queryParams.createTimeBeg = dataarr[0]
      this.queryParams.createTimeEnd = dataarr[1]
    },
    /** 查询流程定义列表 */
    getList() {
      this.loading = true
      myProcessList(this.queryParams).then((response) => {
        this.myProcessList = response.data.records
        this.total = response.data.total
        this.loading = false
      })
    },
    // 取消按钮
    cancel() {
      this.open = false
      this.reset()
    },
    // 表单重置
    reset() {
      this.form = {
        id: null,
        name: null,
        category: null,
        key: null,
        tenantId: null,
        deployTime: null,
        derivedFrom: null,
        derivedFromRoot: null,
        parentDeploymentId: null,
        engineVersion: null,
      }
      this.resetForm('form')
    },
    /** 搜索按钮操作 */
    handleQuery() {
      this.queryParams.pageNum = 1
      this.getList()
    },
    /** 重置按钮操作 */
    resetQuery() {
      this.resetForm('queryForm')
      this.showTimetime = []
      this.queryParams.createTimeBeg = ''
      this.queryParams.createTimeEnd = ''
      this.handleQuery()
    },
    // 多选框选中数据
    handleSelectionChange(selection) {
      this.ids = selection.map((item) => item.id)
      this.single = selection.length !== 1
      this.multiple = !selection.length
    },
    /** 新增按钮操作 */
    handleAdd() {
      this.open = true
      this.title = '发起流程'
      this.listDefinition()
    },
    listDefinition() {
      listDefinition(this.queryParams).then((response) => {
        this.definitionList = response.data.records
        this.processTotal = response.data.total
        this.processLoading = false
      })
    },
    /**  发起流程申请 */
    handleStartProcess(row) {
      localStorage.setItem('finished', true)
      localStorage.setItem('proData', '')
      this.$router.push({
        path: '/flowable/task/record/index',
        query: {
          deployId: row.deploymentId,
          procDefId: row.id,
          formId: row.formId,
          finished: false,
        },
      })
    },
    /**  取消流程申请 */
    handleStop(row) {
      const params = {
        instanceId: row.procInsId,
      }
      stopProcess(params).then((res) => {
        this.msgSuccess(res.msg)
        this.getList()
      })
    },
    /** 流程流转记录 */
    handleFlowRecord(row) {
      console.log(row);
      localStorage.setItem('finished', false)
      getReqAction('/flowable/form/getFormInfoByDeployId', {
        deployId: row.deployId,
      }).then((response) => {
        this.$router.push({
          path: response.data.formRouterPath,
          query: {
            procInsId: row.procInsId,
            deployId: row.deployId,
            taskId: row.taskId,
            procDefId: row.procDefId,
          },
        })
      })
    },
    /** 修改按钮操作 */
    handleUpdate(row) {
      this.reset()
      const id = row.id || this.ids
      getDeployment(id).then((response) => {
        this.form = response.data
        this.open = true
        this.title = '修改流程定义'
      })
    },
    /** 提交按钮 */
    submitForm() {
      this.$refs['form'].validate((valid) => {
        if (valid) {
          if (this.form.id != null) {
            updateDeployment(this.form).then((response) => {
              this.msgSuccess('修改成功')
              this.open = false
              this.getList()
            })
          } else {
            addDeployment(this.form).then((response) => {
              this.msgSuccess('新增成功')
              this.open = false
              this.getList()
            })
          }
        }
      })
    },
    /** 删除按钮操作 */
    handleDelete(row) {
      const ids = row.id || this.ids
      this.$confirm(
        '是否确认删除流程定义编号为"' + ids + '"的数据项?',
        '警告',
        {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning',
        }
      )
        .then(function () {
          return delDeployment(ids)
        })
        .then(() => {
          this.getList()
          this.msgSuccess('删除成功')
        })
    },
    /** 导出按钮操作 */
    handleExport() {
      const queryParams = this.queryParams
      this.$confirm('是否确认导出所有流程定义数据项?', '警告', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning',
      })
        .then(function () {
          return exportDeployment(queryParams)
        })
        .then((response) => {
          this.download(response.msg)
        })
    },
  },
}
</script>

